package Client.SensorData;

public class Coordinates {

	private double lat;
	private double lon;
	
	public double getLat() {
		return lat;
	}
	
	public double getLong() {
		return lon;
	}
	
	public void setLat(double latitude){
		lat = latitude;
	}
	
	public void setLong(double longitude) {
		lon = longitude;
	}
}
